#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char *cs;

	cs = (char *)(s);
	while (n)
	{
		if (*cs == (char)(c))
			return ((void *)s);
		++cs;
		++s;
		--n;
	}
	return (NULL);
}

#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*temp;

	if (lst && f)
	{
		while (lst)
		{
			temp = lst->next;
			(f)(lst);
			lst = temp;
		}
	}
}
